# A Simple module to print greetings.
module Greeting
  WELCOME = 'Welcome..!!'

  def self.hello
    puts 'Hi my friend..!'
  end

  def self.good_morning
    puts 'Good Morning my friend..!'
  end

  def self.good_afternoon
    puts 'Good Afternoon my friend..!'
  end

  def self.good_evening
    puts 'Good Evening my friend..!'
  end
end
