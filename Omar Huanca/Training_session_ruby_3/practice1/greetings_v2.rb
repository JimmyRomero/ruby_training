# A Simple module to print informal greetings.
module Greeting2

  def self.bye
    puts 'See you later.. my friend..!'
  end

  def self.take_care
    puts 'Take case, my friend..!'
  end

  def self.bye_bye
    puts 'Bye Bye my friend..!'
  end

  def self.see_you
    puts 'c ya my friend..!'
  end
end
